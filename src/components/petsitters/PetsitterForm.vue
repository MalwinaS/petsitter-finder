<template>
  <form @submit.prevent="submitForm">
    <div class="form-control">
      <label for="firstname">Firstname</label>
      <input type="text" id="firstname" v-model.trim="firstName" />
    </div>
    <div class="form-control">
      <label for="lastname">Lastname</label>
      <input type="text" id="lasttname" v-model.trim="lastName" />
    </div>
    <div class="form-control">
      <label for="description">Description</label>
      <textarea
        type="text"
        id="description"
        rows="5"
        v-model.trim="description"
      ></textarea>
    </div>
    <div class="form-control">
      <label for="rate">Daily Rate</label>
      <input type="number" id="rate" v-model.number="rate" />
    </div>
    <div class="form-control">
      <h3>Kind of animal</h3>
      <div>
        <input type="checkbox" id="dog" value="dog" v-model="areas" />
        <label for="dog">Dog</label>
      </div>
      <div>
        <input type="checkbox" id="cat" value="cat" v-model="areas" />
        <label for="cat">Cat</label>
      </div>
      <div>
        <input type="checkbox" id="hamster" value="hamster" v-model="areas" />
        <label for="hamster">Hamster</label>
      </div>
    </div>
    <base-button>Register</base-button>
  </form>
</template>

<script>
export default {
  emits: ["save-data"],
  name: "petsitter-form",
  data() {
    return {
      firstName: "",
      lastName: "",
      description: "",
      rate: null,
      areas: [],
    };
  },
  methods: {
    submitForm() {
      const formData = {
        first: this.firstName,
        last: this.lastName,
        desc: this.description,
        rate: this.rate,
        areas: this.areas,
      };
      this.$emit("save-data", formData);
    },
  },
};
</script>

